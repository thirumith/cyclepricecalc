package com.example.demo.service;

import java.sql.Date;

public class Wheel {
	  private String rim;
	  private Date rimDate;
	  private String spokes;
	  private Date spokesDate;
	  private String tube;
	  private Date tubeDate;
	  private String tyre;
	  private Date tyreDate;
	public String getRim() {
		return rim;
	}
	public void setRim(String rim) {
		this.rim = rim;
	}
	public Date getRimDate() {
		return rimDate;
	}
	public void setRimDate(Date rimDate) {
		this.rimDate = rimDate;
	}
	public String getSpokes() {
		return spokes;
	}
	public void setSpokes(String spokes) {
		this.spokes = spokes;
	}
	public Date getSpokesDate() {
		return spokesDate;
	}
	public void setSpokesDate(Date spokesDate) {
		this.spokesDate = spokesDate;
	}
	public String getTube() {
		return tube;
	}
	public void setTube(String tube) {
		this.tube = tube;
	}
	public Date getTubeDate() {
		return tubeDate;
	}
	public void setTubeDate(Date tubeDate) {
		this.tubeDate = tubeDate;
	}
	public String getTyre() {
		return tyre;
	}
	public void setTyre(String tyre) {
		this.tyre = tyre;
	}
	public Date getTyreDate() {
		return tyreDate;
	}
	public void setTyreDate(Date tyreDate) {
		this.tyreDate = tyreDate;
	}
	  
}
